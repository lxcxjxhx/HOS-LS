def get_translation(lang: str, key: str) -> str:
    """获取翻译

    Args:
        lang: 语言代码 (zh/en)
        key: 翻译键

    Returns:
        翻译后的字符串，如果未找到则返回key
    """
    return TRANSLATIONS.get(lang, TRANSLATIONS["zh"]).get(key, key)


def get_state_translation(lang: str, state: str) -> str:
    """获取状态值翻译

    Args:
        lang: 语言代码
        state: 状态值

    Returns:
        翻译后的状态值
    """
    return STATE_TRANSLATIONS.get(lang, STATE_TRANSLATIONS["zh"]).get(state, state)


def get_severity_translation(lang: str, severity: str) -> str:
    """获取严重程度翻译

    Args:
        lang: 语言代码
        severity: 严重程度

    Returns:
        翻译后的严重程度
    """
    return SEVERITY_TRANSLATIONS.get(lang, SEVERITY_TRANSLATIONS["zh"]).get(severity, severity)


def translate_boolean(value: bool, lang: str = "zh") -> str:
    """翻译布尔值

    Args:
        value: 布尔值
        lang: 语言代码

    Returns:
        翻译后的布尔值字符串
    """
    if lang == "en":
        return "Yes" if value else "No"
    return "是" if value else "否"
